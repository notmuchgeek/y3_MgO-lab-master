  integer(i4)                                       :: mmv
  integer(i4)                                       :: msv
  integer(i4)                                       :: mtsv      ! Number of vibrational modes plus shell coordinates = mtv + msv
  integer(i4)                                       :: mtv       ! Number of vibrational modes = mcv + mvv
  integer(i4),  dimension(:),     allocatable       :: mtvptr
  integer(i4),  dimension(:),     allocatable       :: mtvrptr
  integer(i4)                                       :: nd
  integer(i4)                                       :: nf
