!   2/03 Matrix inversion accelerated through packed storage
!  11/04 Pi accessed from module
!   5/06 Mass now uses species values
!   5/07 Calculation of mean kinetic energy added
!   5/07 Partial occupancy data moved to module
!  12/07 Unused variables removed
!   4/08 Finite difference option added for dynamical matrix at
!        gamma point.
!   6/08 Non-analytical correction suppressed for finite difference
!        calculation.
!   1/09 Call to outfrc removed since it outputs incorrect forces
!   3/09 Setting of diagonal blocks skipped for finite difference case
!   3/09 dynamicn now called if this is a finite difference run
!   4/09 Control of calling outphon modified so that it occurs even if noden keyword is present
!   6/09 Integer formats increased to allow for greater numbers of K points
!   6/09 PDF changes added
!   1/10 Output of equipartition free energy added
!  10/10 Handling of nudpt/nldpt corrected so that dispersion lines are identified
!        for non-analytic correction.
!   2/12 Index for freq in equipartition free energy corrected from k to nk
!   6/12 nobsmodeptr0 and nobsmode added as input
!   6/12 Option to call either eispack or lapack eigensolvers added
!   6/12 Allocation of eigr modified for case when lapack eigensolver is being used
!   6/12 Thermal conductivity calculation added
!   6/12 Calls to phoncopy routines changed to add extra argument
!   7/12 nphonat variables moved to module for general use
!   7/12 nphonat variables now computed in a subroutine setphonptr
!   7/12 fobsmodeover added to store overlap information
!   7/12 Oscillator strengths passed to peigeng for IR intensity calculation
!   8/12 Call to oscillator strength added for non-gamma routine
!   8/12 Call to peigen modified
!  10/12 Variable number of decimal places added for frequency file output
!   1/13 Non-analytic correction turned off for thermal conductivity as this overwrites derv2.
!   7/13 Handling of non-analytical correction for integer k points added
!   9/13 Raman susceptibility for modes added as an option
!   9/13 Calculation of thermodynamics and printing moved to a subroutine vibenergy
!   9/13 ramstrength passed to peigeng
!   9/13 Output of Raman directions added
!  10/13 Call to ramanstrength added
!  12/13 CASTEP phonon output added
!  12/13 leig removed from argument list to peigen(g)
!   2/14 Integer kpoint flag corrected to factors of 2. 
!   4/14 Non-analytic correction excluded for a single k point unless the direction of
!        approach has been input
!   8/14 Call to changemaxfreqat added
!   8/14 k point index passed to phoncopy2 routines / pdiagl
!   8/14 Compression of derv2/dervi now uses compressd2 subroutine
!   9/14 Logic corrected for savederv2 allocation related to sense of lnonanal changing
!   9/14 Saving and adding back of breathing shell diagonal blocks removed since the
!        self terms are computed by realp on each call
!  10/14 Changed so that fractional k point is passed to dynamic rather than k point
!        index
!  10/14 Call added to handle shells in group velocity calculation
!   2/15 Call to outshengBTE moved here from optim
!   4/15 lusefc added to call to outshengBTEcontrol
!   5/15 Analytical calculation of third order force constants added
!   7/15 OutshengBTE work moved to the end of the routine since it
!        no longer relies on the pre-existing state of the second
!        derivatives
!  12/15 thermalconductivity renamed to thermalconductivity_af
!   2/16 Use of nllkpt for write statement corrected to nlkpt
!   7/16 lshengBTE3rd added
!   8/16 IR intensities now stored in global array
!   9/16 Logical around allocation & deallocation of savederv2/savedervi corrected
!   9/16 nphonatptr no longer allocated or deallocated since it is in a module
!  11/16 Modifications for parallel second derivatives started
!  12/16 mcvrptr added to pass to lower
!  12/16 mcvptr added to pass to oscillatorstrengthg
!  12/16 Merge cluster frequency functionality into this routine
!  11/17 leigloc set to true for MSD calc
!  11/17 wk now passed to peigen
!   1/18 Grueneisen parameters added
!   1/18 Set up of fourbody list added for Grueneisen calculation
!   1/18 Trace added
!   3/18 dynam shortened to dyna in keyword check
!   4/18 leigloc now set here for llower and linten
!   3/19 Multiple temperature ramps added
!   5/19 Finite difference flag split for first and second derivatives
!   8/19 Uncompress imaginary dynamical matrix only written if not gamma
!   8/19 Modifications to breathing shell compression in response to 
!        change in storage pattern in realp
!   8/19 Handling of partial occupancy compression corrected
!   9/19 nrel2 / nrelat changed to nrela2f / nrelf2a
!   4/20 Rigid molecule modifications added
!   4/20 maxfqat changed to maxfreq since it is no longer just atom based
!   4/20 Mass arrays now use fmass and rfmass
!   4/20 Use of constant to test whether a number is too small added
!   5/20 Handling of diagonal blocks added for rigid molecules
!   5/20 Calculation of molaxes added
!   5/20 mcvptr/mcvrptr changed to mtvptr/mtvrptr
!   5/20 ncfoc removed from arguments to groupvelocities
!   6/20 Correction to integerKpoint logic
!   6/20 Calls to lower removed except for gamma point
!   6/20 Last argument to outphon changed
!   6/20 Criteria for being gamma point made consistent
!   6/20 nmolcore changes added
!   6/20 Order of operations changed - shells now handled before
!        generating molecular terms so that eckart works with shells
!   7/20 bornk passed as local array
