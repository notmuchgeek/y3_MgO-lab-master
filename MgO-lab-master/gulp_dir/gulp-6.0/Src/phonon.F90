!  Local variables
!
  character(len=5)                                  :: lab1
  character(len=2)                                  :: fstring1
  character(len=2)                                  :: fstring2
  character(len=12)                                 :: fstring
  complex(dpc), dimension(:),     allocatable       :: ctmp
  integer(i4)                                       :: i
  integer(i4)                                       :: ifail
  integer(i4)                                       :: ii
  integer(i4)                                       :: im
  integer(i4)                                       :: indi
